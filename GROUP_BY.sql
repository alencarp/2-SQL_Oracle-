/* OS CAMPOS QUE NÃO ESTAO NO CRITÉRIO DE AGREGAÇÃO, OU SEJA, NO 'GROUP BY', DEVEM SER NUMÉRICOS, 
 QUE NESTE CASO É A 'IDADE', ENTÃO APLICA-SE 'a formula' DE AGREGAÇÃO NELE (SUM, AVG, MIN, MAX, COUNT)
 E NO ORDER BY, NÃO SE COLOCA O CAMPO NUMÉRICO QUE ESTÁ SENDO USADO PARA FAZER A AGREGAÇÃO (NESTE CASO 'IDADE')
*/
SELECT CIDADE, SUM(IDADE) FROM TABELA_DE_CLIENTES GROUP BY CIDADE ORDER BY CIDADE;
SELECT CIDADE, AVG(IDADE) FROM TABELA_DE_CLIENTES GROUP BY CIDADE ORDER BY CIDADE;
SELECT CIDADE, ROUND(AVG(IDADE),2) FROM TABELA_DE_CLIENTES GROUP BY CIDADE ORDER BY CIDADE;

