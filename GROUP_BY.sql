/* QUANDO UTILIZAR O 'GROUP BY', NA LISTA DE CAMPOS NUMÉRICOS QUE VOCÊ ESTÁ ESCOLHENDO PARA AGRUPAR,
   COLOCAR A FÓRMULA MATEMÁTICA QUE VAI DIZER SE VOU SOMAR, CALCULAR A MÉDIA, ETC.:(SUM, AVG, MIN, MAX, COUNT)
*/

-- VOCÊ SÓ PÕE NO 'GROUP BY' OS CAMPOS NÃO NUMÉRICOS
-- E PARA FAZER A 'SUM, AVG, MIN, MAX' VOCÊ SÓ PÕE CAMPOS NUMÉRICOS

SELECT * FROM TABELA_DE_CLIENTES;

SELECT ESTADO, 
       SUM(VOLUME_DE_COMPRA) SOMA
FROM TABELA_DE_CLIENTES
GROUP BY ESTADO;

SELECT ESTADO, 
       MIN(VOLUME_DE_COMPRA) MINIMO
FROM TABELA_DE_CLIENTES
GROUP BY ESTADO;

SELECT ESTADO, 
       MAX(VOLUME_DE_COMPRA) MAXIMO
FROM TABELA_DE_CLIENTES
GROUP BY ESTADO;

SELECT ESTADO, 
       AVG(VOLUME_DE_COMPRA) MEDIA
FROM TABELA_DE_CLIENTES
GROUP BY ESTADO;

SELECT ESTADO, 
       ROUND(AVG(VOLUME_DE_COMPRA), 2) MEDIA
FROM TABELA_DE_CLIENTES
GROUP BY ESTADO;
